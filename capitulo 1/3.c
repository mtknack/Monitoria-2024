#include <stdio.h>

int main() {
    // Definindo os números
    int num1 = 5, num2 = 8, num3 = 12;
    
    // Calculando a média aritmética
    float media = (num1 + num2 + num3) / 3.0;
    
    // Imprimindo o resultado
    printf("A media aritmetica entre %d, %d e %d eh: %.2f\n", num1, num2, num3, media);
    
    return 0;
}
